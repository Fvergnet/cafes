auto getMatElemLaplacian(std::array<double, 2> const& h){
  std::array<double, 16> MatElem;
  double hxy = h[0]/h[1], hyx = h[1]/h[0];

  for(std::size_t i=0; i<16; ++i)
    MatElem[i] = hyx*matElem2d_dxudxu[i] + hxy*matElem2d_dyudyu[i];
  
  return MatElem;
}

auto getMatElemStrainTensor(std::array<double, 2> const& h){
  std::array<std::array<double, 4>, 16> MatElem;
  double hxy = h[0]/h[1], hyx = h[1]/h[0];

  for(std::size_t i=0; i<16; ++i){
    MatElem[i][0] = 2*hyx*matElem2d_dxudxu[i] + hxy*matElem2d_dyudyu[i];
    MatElem[i][1] = matElem2d_dxudyu[i];
    MatElem[i][3] = hyx*matElem2d_dxudxu[i] + 2*hxy*matElem2d_dyudyu[i];
  }

  for(std::size_t i=0; i<4; ++i)
    for(std::size_t j=0; j<4; ++j)
      MatElem[j*4+i][2] = MatElem[i*4+j][1];

  return MatElem;
}

auto getMatElemPressure(std::array<double, 2> const& h){
  // return the integral of p div u
  std::array<std::array<double, 2>, 36> MatElem;

  for(std::size_t i=0; i<36; ++i){
    MatElem[i][0] = -h[1]*matElem2d_pdxu[i];
    MatElem[i][1] = -h[0]*matElem2d_pdyu[i];
  }

  return MatElem;
}

auto getMatElemMass(std::array<double, 2> const& h){
  std::array<double, 16> MatElem;

  for(std::size_t i=0; i<16; ++i)
    MatElem[i] = h[0]*h[1]*matElemMass2d[i];

  return MatElem;  
}

auto getMatElemLaplacian(std::array<double, 3> const& h){
  std::array<double, 64> MatElem;
  double hxy = h[0]*h[1], hxz = h[0]*h[2], hyz = h[1]*h[2];

  for(std::size_t i=0; i<64; ++i)
    MatElem[i] = hyz/h[0]*matElem3d_dxudxu[i] + hxz/h[1]*matElem3d_dyudyu[i] + hxy/h[2]*matElem3d_dzudzu[i];
  
  return MatElem;
}

auto getMatElemStrainTensor(std::array<double, 3> const& h){
  double hxy = h[0]*h[1], hxz = h[0]*h[2], hyz = h[1]*h[2];
  std::array<std::array<double, 9>, 64> MatElem;

  for(std::size_t i=0; i<64; ++i){
    MatElem[i][0] = 2*hyz/h[0]*matElem3d_dxudxu[i] + hxz/h[1]*matElem3d_dyudyu[i] + hxy/h[2]*matElem3d_dzudzu[i];
    MatElem[i][4] = hyz/h[0]*matElem3d_dxudxu[i] + 2*hxz/h[1]*matElem3d_dyudyu[i] + hxy/h[2]*matElem3d_dzudzu[i];
    MatElem[i][8] = hyz/h[0]*matElem3d_dxudxu[i] + hxz/h[1]*matElem3d_dyudyu[i] + 2*hxy/h[2]*matElem3d_dzudzu[i];
    MatElem[i][1] = h[2]*matElem3d_dxudyu[i];
    MatElem[i][2] = h[1]*matElem3d_dxudzu[i];
    MatElem[i][5] = h[0]*matElem3d_dyudzu[i];
  }
  
  for(std::size_t i=0; i<8; ++i)
    for(std::size_t j=0; j<8; ++j){
      MatElem[j*8+i][3] = MatElem[i*8+j][1];
      MatElem[j*8+i][6] = MatElem[i*8+j][2];
      MatElem[j*8+i][7] = MatElem[i*8+j][5];
    }   

  return MatElem;
}

auto getMatElemPressure(std::array<double, 3> const& h){
  std::array<std::array<double, 3>, 216> MatElem;

  for(std::size_t i=0; i<216; ++i){
    MatElem[i][0] = -h[1]*h[2]*matElem3d_pdxu[i];
    MatElem[i][1] = -h[0]*h[2]*matElem3d_pdyu[i];
    MatElem[i][2] = -h[0]*h[1]*matElem3d_pdzu[i];
  }

  return MatElem;
}

auto getMatElemMass(std::array<double, 3> const& h){
  std::array<double, 64> MatElem;

  for(std::size_t i=0; i<64; ++i)
    MatElem[i] = h[0]*h[1]*h[2]*matElemMass3d[i];

  return MatElem;
}
